public class ScanResult {
    private Integer egg;
    private Integer grain;
    private Integer wood;
    private Integer fence;

    public Integer getEgg() {
        return egg;
    }

    public void setEgg(Integer egg) {
        this.egg = egg;
    }

    public Integer getGrain() {
        return grain;
    }

    public void setGrain(Integer grain) {
        this.grain = grain;
    }

    public Integer getWood() {
        return wood;
    }

    public void setWood(Integer wood) {
        this.wood = wood;
    }

    public Integer getFence() {
        return fence;
    }

    public void setFence(Integer fence) {
        this.fence = fence;
    }
}
